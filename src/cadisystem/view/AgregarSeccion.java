    private void btnPlanificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlanificarActionPerformed
        String msj;
        getFechaTentativaInicio();
        Asesor ase = (Asesor) cmbAsesor.getSelectedItem();
        if (compararDosFechas() == null) {
            msj = "Debe seleccionar una fecha Tentativa de Inicio ";
            JOptionPane.showMessageDialog(null, msj, "Error", JOptionPane.ERROR_MESSAGE);
        } else if (!compararDosFechas()) {
            msj = "Seleccione una fecha mayor o igual a la de hoy ";
            JOptionPane.showMessageDialog(null, msj, "Error", JOptionPane.ERROR_MESSAGE);
        }        else if ("".equals(cursoValue.getText())) {
                    msj = "Seleccione un curso del catálogo";
                    JOptionPane.showMessageDialog(null, msj, "Error", JOptionPane.ERROR_MESSAGE);
              } 
        else if (ase.getId() == -1) {
            msj = "Seleccione un asesor";
            JOptionPane.showMessageDialog(null, msj, "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            SeccionDAO seccdao = new SeccionImp();
            Seccion seccion = new Seccion();
            seccion.getCurso().setId(cursoSelected.getId());
            seccion.setFechaRegistro(hoy);
            seccion.setFechaTentativaInicio(inicio);
            seccion.getAsesor().setId(ase.getId());
            seccion.getEstado().setId(1);

            try {
                // Inserta los datos de la seccion
                seccdao.insertar(seccion);
                msj = "Ha creado la sección satisfactoriamente";
                // id de la seccion que acabo de registrar
                Integer id = seccdao.obtenerUltimoIdSeccion();
                SeccionMovimiento seccEst = new SeccionMovimiento("Planificada","", hoy,id);
                SeccionMovimientoDAO seccEstImp = new SeccionMovimientoImp();
                seccEstImp.insertar(seccEst);
//  METODO PARA LOS MOVIMIENTOS DE SECCION
//                SeccionHistoricoDAO seccHdao = new SeccionHistoricoImp();
//                String desc = "la sección fué creada el " + lblFechaRegistroValue.getText();
//                SeccionHistorico seccH = new SeccionHistorico(desc, id);
//                seccHdao.insertar(seccH);
                JOptionPane.showMessageDialog(null, msj, "Información", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            } catch (Exception ex) {
                Logger.getLogger(AgregarSeccion.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnPlanificarActionPerformed

