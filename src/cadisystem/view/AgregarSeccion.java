    public Integer ObtenerUltimoIdMasUno() {
        Integer id = -2;
        SeccionDAO secciondao = new SeccionImp();
        try {
            id = secciondao.obtenerUltimoIdSeccion();
        } catch (Exception ex) {
            Logger.getLogger(AgregarSeccion.class.getName()).log(Level.SEVERE, null, ex);
        }
        return id + 1;
    }

