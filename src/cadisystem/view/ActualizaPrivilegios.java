    public Usuario buscarUsuario() {
        UsuarioDAO userDAO = new UsuarioImp();
        Usuario user;

        try {
            user = userDAO.buscar(String.valueOf(this.txtUsuario.getText()));

        } catch (Exception ex) {
            user = null;
        }
        return user;
    }

    public List<UsuarioPrivilegio> buscarPrivilegiosDadoUsuario(Integer usuario) {
        List<UsuarioPrivilegio> lista = new ArrayList();
        UsuarioPrivilegioDAO usrp = new UsuarioPrivilegioImp();
        try {
            lista = usrp.buscarPrivilegiosDadoUsuario(usuario);
        } catch (Exception ex) {
            Logger.getLogger(ActualizaPrivilegios.class.getName()).log(Level.SEVERE, null, ex);
        }

        return lista;
    }

    public void seleccionado(javax.swing.JCheckBox jCheckBox, String status) {

        if (status.equals("A")) {

            jCheckBox.setSelected(true);

        } else {

            jCheckBox.setSelected(false);

        }

    }

    public Boolean eliminarCheckBox(String desc) {
        Boolean valor = null;
        for (int i = 0; i < listado.size(); i++) {

            if (desc.equals(listado.get(i).getNombrePrivilegio())) {
                listado.get(i).setStatus("E");
                valor = true;
            } else {
                valor = false;
            }

        }

        return valor;
    }

