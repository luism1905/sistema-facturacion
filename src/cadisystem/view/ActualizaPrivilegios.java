    private void btnGuadrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuadrarActionPerformed

        UsuarioPrivilegioDAO usrdao = new UsuarioPrivilegioImp();
        Integer idUusuarioPriv = listado.get(0).getIdUsuario();
        Integer contador = 0;
        for (int i = 0; i < listado.size(); i++) {

            try {

                usrdao.actualizarEstatusPrivilegios(listado.get(i).getId(), listado.get(i).getStatus());
                System.out.println(listado.get(i).getStatus());
                contador++;
            } catch (Exception ex) {
                Logger.getLogger(ActualizaPrivilegios.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

        if (contador.equals(listado.size())) {

            String msj = "Ha cambiado los privilegios satisfactoriamente";
            JOptionPane.showMessageDialog(null, msj, "Información", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();

        }


    }//GEN-LAST:event_btnGuadrarActionPerformed

