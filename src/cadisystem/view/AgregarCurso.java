    private void bntGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntGuardarActionPerformed
        if (!validarCampos()) {
            mostrarMensaje();
        } else {

            Curso curso = new Curso(Integer.valueOf(lblNumeroValue.getText()),txtNombre.getText(), Integer.valueOf(txtDuracion.getText()), Float.valueOf(txtPrecio.getText()));
             
            CursoDAO cursodao = new CursoImp();
          //  cursodao.
            if(cursoEncontrado){
                
                try {
                    cursodao.modificar(curso);
                    String msj = "Ha actualizado la información del curso";
                    JOptionPane.showMessageDialog(null, msj, "Información", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                    
                } catch (Exception ex) {
                    Logger.getLogger(AgregarCurso.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            }
            else{
                try {
                    cursodao.insertar(curso);
                } catch (Exception ex) {
                    Logger.getLogger(AgregarCurso.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_bntGuardarActionPerformed

    private void txtNombreFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtNombreFocusLost
        CursoDAO cursodao = new CursoImp();
        Vector<Curso> LCurso = null;
        try {
            LCurso = cursodao.listar();
        } catch (Exception ex) {
            Logger.getLogger(AgregarCurso.class.getName()).log(Level.SEVERE, null, ex);
        }
        for (Curso curs : LCurso) {
            if (curs.getNombre().equals(txtNombre.getText())) {
             txtPrecio.setText(String.valueOf(curs.getPrecio()));
             txtDuracion.setText(String.valueOf(curs.getHoras()));
             lblNumeroValue.setText(String.valueOf(curs.getId()));
             cursoEncontrado = true;
             
            } else {

                cursoEncontrado = false;
            }
        }
    }//GEN-LAST:event_txtNombreFocusLost

