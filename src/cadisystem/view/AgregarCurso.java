public void ObtenerUltimoId() {
        Integer id = null;
        CursoDAO cursodao = new CursoImp();
        try {
            id = cursodao.obtenerUltimoIdCurso();
        } catch (Exception ex) {
            Logger.getLogger(AgregarCurso.class.getName()).log(Level.SEVERE, null, ex);
        }
        id++;
        lblNumeroValue.setText(String.valueOf(id));
    }

    public Boolean validarCampos() {
        if (txtNombre.getText().equals("") || txtDuracion.getText().equals("")
                || txtPrecio.getText().equals("")) {
            return false;
        } else {
            return true;
        }
    }

    public void mostrarMensaje() {

        String msj;

        if ("".equals(txtNombre.getText())) {
            msj = "Introduzca el nombre del curso";
            JOptionPane.showMessageDialog(null, msj, "Error", JOptionPane.ERROR_MESSAGE);

        } else if ("".equals(txtPrecio.getText())) {
            msj = "Introduzca el precio del curso";
            JOptionPane.showMessageDialog(null, msj, "Error", JOptionPane.ERROR_MESSAGE);

        } else if ("".equals(txtNombre.getText())) {
            msj = "Introduzca el nombre del Asesor";
            JOptionPane.showMessageDialog(null, msj, "Error", JOptionPane.ERROR_MESSAGE);

        } else if ("".equals(txtDuracion.getText())) {
            msj = "Introduzca la duración del curso";
            JOptionPane.showMessageDialog(null, msj, "Error", JOptionPane.ERROR_MESSAGE);

        }
    }
